function getNestedValue(obj, path) {
  return path.split('.').reduce(function(curr, key) {
    return curr && curr[key] !== undefined ? curr[key] : undefined;
  }, obj);
}

function applyTranslations(lang) {
  var t = translations[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    var value = getNestedValue(t, key);
    if (value !== undefined) {
      el.innerHTML = value;
    }
  });

  // Document lang
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';

  // Page title
  document.title = t.meta.title;

  // Meta description
  var metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute('content', t.meta.description);

  // OG tags
  var ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle) ogTitle.setAttribute('content', t.meta.ogTitle);
  var ogDesc = document.querySelector('meta[property="og:description"]');
  if (ogDesc) ogDesc.setAttribute('content', t.meta.ogDesc);

  // Twitter tags
  var twTitle = document.querySelector('meta[property="twitter:title"]');
  if (twTitle) twTitle.setAttribute('content', t.meta.ogTitle);
  var twDesc = document.querySelector('meta[property="twitter:description"]');
  if (twDesc) twDesc.setAttribute('content', t.meta.twDesc);

  // HTML data-lang attribute (for CSS hooks if needed)
  document.documentElement.setAttribute('data-lang', lang);
}

function setActiveLang(lang) {
  var ptEl = document.getElementById('langPT');
  var enEl = document.getElementById('langEN');
  if (ptEl) ptEl.classList.toggle('active', lang === 'pt');
  if (enEl) enEl.classList.toggle('active', lang === 'en');
}

document.addEventListener('DOMContentLoaded', function() {
  var saved = localStorage.getItem('vs-lang') || 'pt';
  currentLang = saved;
  applyTranslations(saved);
  setActiveLang(saved);

  var langToggle = document.getElementById('langToggle');
  if (langToggle) {
    langToggle.addEventListener('click', function() {
      var newLang = currentLang === 'pt' ? 'en' : 'pt';
      currentLang = newLang;
      localStorage.setItem('vs-lang', newLang);
      applyTranslations(newLang);
      setActiveLang(newLang);
    });
  }
});
